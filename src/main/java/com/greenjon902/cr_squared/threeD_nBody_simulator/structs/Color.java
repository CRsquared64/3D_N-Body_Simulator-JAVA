package com.greenjon902.cr_squared.threeD_nBody_simulator.structs;

public class Color {
    private final float r;
    private final float g;
    private final float b;

    public Color(float r, float g, float b) {
        this.r = r;
        this.g = g;
        this.b = b;
    }

    public float getR() {
        return r;
    }

    public float getG() {
        return g;
    }

    public float getB() {
        return b;
    }
}
